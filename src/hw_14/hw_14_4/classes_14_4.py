class Math:
    def __init__(self, x, y):
        self.x = x
        self.y = y

    def addition(self):
        return self.x + self.y

    def substraction(self):
        return self.x - self.y

    def multiply(self):
        return self.x * self.y

    def division(self):
        return self.x / self.y
